void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int	i;
	int n;

	i = 1;
	while (i < argc)
	{
		n = 0;
		while (argv[i][n] != '\0')
		{
			ft_putchar(argv[i][n]);
			n++;
		}
		ft_putchar('\n');
		i++;
	}
}
